# Задание 5: "Словарь синонимов" (Dict + get)

def main():
    print("=" * 50)
    print("ПРОГРАММА-ПОМОЩНИК ДЛЯ ПИСАТЕЛЯ")
    print("=" * 50)

    # 1. Создаем словарь синонимов
    synonyms_dict = {
        # Внешность
        "красивый": "прекрасный",
        "уродливый": "безобразный",
        "молодой": "юный",
        "старый": "пожилой",

        # Характер
        "добрый": "великодушный",
        "злой": "жестокий",
        "умный": "мудрый",
        "глупый": "недалекий",

        # Действия
        "быстрый": "стремительный",
        "медленный": "неторопливый",
        "сильный": "могучий",
        "слабый": "бессильный",

        # Предметы
        "большой": "огромный",
        "маленький": "крошечный",
        "дорогой": "ценный",
        "дешевый": "бюджетный"
    }

    print(f"В словаре доступно {len(synonyms_dict)} слов")
    print("Примеры: " + ", ".join(list(synonyms_dict.keys())[:5]) + "...")
    print()

    # 2. Запрашиваем слово у пользователя
    while True:
        user_word = input("Введите слово для поиска синонима (или 'выход'): ").strip()

        if user_word.lower() in ['выход', 'exit', 'quit']:
            print("\nСпасибо за использование программы!")
            break

        if not user_word:
            print("Пожалуйста, введите слово!")
            continue

        # 3. Используем метод .get() для поиска синонима
        synonym = synonyms_dict.get(user_word.lower(), user_word)

        # 4. Выводим результат
        print("-" * 40)
        if synonym != user_word:
            print(f"✓ Найден синоним для '{user_word}':")
            print(f"  '{user_word}' → '{synonym}'")
        else:
            print(f"✗ Слово '{user_word}' не найдено в словаре.")
            print(f"  Используйте: '{synonym}'")
        print("-" * 40)
        print()


if __name__ == "__main__":
    main()