# 1. Запрашиваем имя и фамилию у пользователя
full_name = input("Введите имя и фамилию (через пробел): ")

# 2. Разделяем строку на имя и фамилию
name, surname = full_name.split()

# 3. Генерируем никнейм в нужном формате
# Приводим все к нижнему регистру и берем первую букву имени
nickname = f"{surname.lower()}_{name[0].lower()}"

# 4. Выводим результат
print(f"Ваш логин: {nickname}")
