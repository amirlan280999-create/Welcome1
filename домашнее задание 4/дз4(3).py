class ATM:
    def __init__(self, initial_balance: int, pin: str):
        self.__balance = initial_balance
        self.pin = pin  #–ò—Å–ø–æ–ª—å–∑—É–µ–º —Å–µ—Ç—Ç–µ—Ä –¥–ª—è –≤–∞–ª–∏–¥–∞—Ü–∏–∏ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏
        self.__failed_attempts = 0
        self.__blocked = False

    @property
    def pin(self) -> str:
        """–ì–µ—Ç—Ç–µ—Ä: —Å–∫—Ä—ã–≤–∞–µ—Ç –Ω–∞—Å—Ç–æ—è—â–∏–π –ü–ò–ù-–∫–æ–¥"""
        return "Redacted"

    @pin.setter
    def pin(self, new_pin: str):
        """–°–µ—Ç—Ç–µ—Ä: –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –ü–ò–ù —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –æ–Ω —Å–æ—Å—Ç–æ–∏—Ç —Ä–æ–≤–Ω–æ –∏–∑ 4 —Ü–∏—Ñ—Ä"""
        if isinstance(new_pin, str) and len(new_pin) == 4 and new_pin.isdigit():
            self.__pin = new_pin
            print("–ü–ò–ù-–∫–æ–¥ —É—Å–ø–µ—à–Ω–æ –æ–±–Ω–æ–≤–ª—ë–Ω.")
        else:
            print("–û—à–∏–±–∫–∞: –ü–ò–ù-–∫–æ–¥ –¥–æ–ª–∂–µ–Ω —Å–æ—Å—Ç–æ—è—Ç—å —Ä–æ–≤–Ω–æ –∏–∑ 4 —Ü–∏—Ñ—Ä!")

    def withdraw(self, amount: int, pin_attempt: str) -> None:
        """–°–Ω—è—Ç–∏–µ –¥–µ–Ω–µ–≥ —Å –ø—Ä–æ–≤–µ—Ä–∫–æ–π –ü–ò–ù-–∫–æ–¥–∞, –±–∞–ª–∞–Ω—Å–∞ –∏ –±–ª–æ–∫–∏—Ä–æ–≤–∫–∏"""
        if self.__blocked:
            print("–ö–∞—Ä—Ç–∞ –∑–∞–±–ª–æ–∫–∏—Ä–æ–≤–∞–Ω–∞.")
            return

        if pin_attempt != self.__pin:
            self.__failed_attempts += 1
            print("–û—à–∏–±–∫–∞: –Ω–µ–≤–µ—Ä–Ω—ã–π –ü–ò–ù-–∫–æ–¥.")
            if self.__failed_attempts >= 3:
                self.__blocked = True
                print("–ö–∞—Ä—Ç–∞ –∑–∞–±–ª–æ–∫–∏—Ä–æ–≤–∞–Ω–∞ –ø–æ—Å–ª–µ 3 –Ω–µ—É–¥–∞—á–Ω—ã—Ö –ø–æ–ø—ã—Ç–æ–∫.")
            return

        #–°–±—Ä–æ—Å —Å—á—ë—Ç—á–∏–∫–∞ –æ—à–∏–±–æ–∫ –ø—Ä–∏ —É—Å–ø–µ—à–Ω–æ–π –ø–æ–ø—ã—Ç–∫–µ
        self.__failed_attempts = 0

        if amount <= 0:
            print("–û—à–∏–±–∫–∞: —Å—É–º–º–∞ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω–æ–π.")
            return

        if amount > self.__balance:
            print("–û—à–∏–±–∫–∞: –Ω–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ —Å—Ä–µ–¥—Å—Ç–≤ –≤ –±–∞–Ω–∫–æ–º–∞—Ç–µ.")
            return

        self.__balance -= amount
        print(f"–í—ã–¥–∞–Ω–æ {amount}.")

    #–º–µ—Ç–æ–¥ –¥–ª—è –≤–Ω—É—Ç—Ä–µ–Ω–Ω–µ–≥–æ –ø—Ä–æ—Å–º–æ—Ç—Ä–∞ –±–∞–ª–∞–Ω—Å–∞ (–Ω–µ –≤–ª–∏—è–µ—Ç –Ω–∞ –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç—å)
    def _get_balance_for_debug(self) -> int:
        return self.__balance


