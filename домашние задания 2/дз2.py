# ЗАДАНИЕ 2: "Анализатор текста" (For + Dict + If)

# 1. Исходный текст (по условию задания)
text = "Python is amazing language"

# 2. Создаем словарь для хранения результатов (по условию)
stats = {"гласные": 0, "согласные": 0}

# 3. Строка с гласными для проверки (по условию)
vowels = "aeiou"

print("=" * 50)
print("АНАЛИЗАТОР ТЕКСТА")
print("=" * 50)
print(f"Текст для анализа: '{text}'")
print("-" * 50)

# 4. Проходим по каждому символу текста циклом for
for char in text:
    # Пропускаем пробелы (требование из условия)
    if char == " ":
        continue  # переходим к следующему символу

    # Приводим символ к нижнему регистру для единообразной проверки
    char_lower = char.lower()

    # Проверяем, является ли символ буквой (улучшение для корректного подсчета)
    if char_lower.isalpha():
        # Проверяем, есть ли символ в строке гласных
        if char_lower in vowels:
            # Увеличиваем счетчик гласных
            stats["гласные"] += 1
        else:
            # Все остальные буквы считаем согласными
            stats["согласные"] += 1
    # Примечание: цифры и знаки препинания игнорируются

# 5. Выводим результаты
print("\nРЕЗУЛЬТАТЫ АНАЛИЗА:")
print(f"├─ Гласные буквы: {stats['гласные']}")
print(f"├─ Согласные буквы: {stats['согласные']}")
print(f"├─ Всего букв: {stats['гласные'] + stats['согласные']}")
print(f"└─ Итоговый словарь: {stats}")
print("=" * 50)

# Дополнительная информация для понимания логики
print("\nПОЯСНЕНИЯ:")
print("1. Пробелы пропускаются с помощью 'continue'")
print("2. Регистр приводится к нижнему для корректной проверки")
print("3. Проверка isalpha() гарантирует, что считаются только буквы")
print("4. Оператор 'in' проверяет наличие символа в строке гласных")